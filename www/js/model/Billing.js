class Billing
{
	// Default constructor
	constructor
	(
		name,
    	country,
		street,
		streetNumber,
		state,
		city,
		neighborhood,
		zipcode
	)
	{
		this.name = name;
		this.country = country;
		this.street = street;
		this.streetNumber = streetNumber;
		this.state = state;
		this.city = city;
		this.neighborhood = neighborhood;
		this.zipcode = zipcode;
	}
}
